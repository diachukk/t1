'''
Запросіть три числа a, b і с. 
Виведіть об'єм прямокутного паралелепіпеда з заданими сторонами в форматі, 
який вказаний в прикладі. 
Формула знаходження об'єму прямокутного паралелепіпеда: 
V = a * b * c
'''

a = int(input("Введіть число першої сторони - ") )
b = int(input(" Введіть число другої сторони - ") )
c = int(input(" Введіть число третої сторони - ") )
V = a * b * c
print("Об'єм прямокутного паралелепіпеда з заданими сторонами = ", V) 
 

        